from math import *

print(2)
print(2.1231231)
print(-2.1231231)
print(2 + 3.45)
print(2 * 3.45)
print(2 / 3.45)
print(2 * 2 + 5)
print(2 * (2 + 5))
print(2 + 2 * 5)

# remainder
print(10 % 3)

my_num = 5

# convert my_num to string
print(my_num, "is my favourite number")

# absolute value
my_num = -5
print(abs(my_num))

# pow
print(pow(3, 2))
print(pow(4, 6))

# max/min
print(max(4, 6))
print(min(4, 6))

# round number
print(round(3.5433))
print(round(8.96434))

# floor function, chop off number after decimal
print(floor(3.789))

# ceil function, always round the number after decimal
print(ceil(3.223))

# square root
print(sqrt(36))
